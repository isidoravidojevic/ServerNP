    private void btnStartServerActionPerformed(java.awt.event.ActionEvent evt) {                                               
        try {
            File file = new File("config/dbconfig.properties");
            boolean exists = file.createNewFile();
            Properties properties = new Properties();
            
            if (exists == false) {
                properties.load(new FileInputStream("config/dbconfig.properties"));
                if(properties.isEmpty()){
                    new ConfigurationForm().setVisible(true);
                    return;
                }
                server = new Server(this);
                jLabel1.setText("Server je pokrenut!");
                jLabel1.setForeground(Color.green);
                jPanel1.setVisible(true);
                btnStopServer.setEnabled(true);
                btnStartServer.setEnabled(false);
                tblUsers.setModel(new UserTableModel());
                tblUsers.getColumnModel().getColumn(0).setMinWidth(150);
                tblUsers.getColumnModel().getColumn(3).setMinWidth(150);
            } else{
                new ConfigurationForm().setVisible(true);
            }
        } catch (IOException ex) {
            Logger.getLogger(ServerForm.class.getName()).log(Level.SEVERE, null, ex);
        }

    }                                              

    private void btnStopServerActionPerformed(java.awt.event.ActionEvent evt) {                                              
        try {
            if (server.getServerSocket() != null && (server.getServerSocket().isBound())) {
                if (server.getUserList().size() != 0 && server.getClientList().size() != 0) {
                    int number = JOptionPane.showConfirmDialog(this, "Postoji klijent koji je vezan za server. Da li ste sigurni da Å¾elite da prekinete konekciju?", "Poruka", JOptionPane.YES_NO_OPTION);
                    if (number == 0) {
                        server.getServerSocket().close();
                        jLabel1.setText("Server je zaustavljen!");
                        jLabel1.setForeground(Color.red);
                        ((UserTableModel) tblUsers.getModel()).clear();
                        jPanel1.setVisible(false);
                        btnStopServer.setEnabled(false);
                        btnStartServer.setEnabled(true);

                    }
                } else {

                    server.getServerSocket().close();
                    jLabel1.setText("Server je zaustavljen!");
                    jLabel1.setForeground(Color.red);
                    ((UserTableModel) tblUsers.getModel()).clear();
                    jPanel1.setVisible(false);
                    btnStopServer.setEnabled(false);
                    btnStartServer.setEnabled(true);
                }
            }
        } catch (IOException ex) {
//            Logger.getLogger(ServerForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }                                             

